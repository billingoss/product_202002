package com.api.model.billing;

/**
 * 채널 청구 정보
 *
 */
public class ChannelBillingInformation {

	private String channelId;					//채널ID
	private String channelName;				//채널Name
	private int providerNumber;				//업체ID
	private String prePaymentYn;			//선불여부
	private String payableYn;					//결제가능여부
	private String invoiceFileCreateYn;		//파일 생성 가능 여부
	private String description;					//설명
	private String useYn;						//사용여부
	private String deleteDate;					//삭제일자
	private String auditId;						//생성자ID
	private String auditDateTime;			//생성일시	
	private String editId;						//수정자ID
	private String editDateTime;				//수정일시
	public String getChannelId() {
		return channelId;
	}
	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public int getProviderNumber() {
		return providerNumber;
	}
	public void setProviderNumber(int providerNumber) {
		this.providerNumber = providerNumber;
	}
	public String getPrePaymentYn() {
		return prePaymentYn;
	}
	public void setPrePaymentYn(String prePaymentYn) {
		this.prePaymentYn = prePaymentYn;
	}
	public String getPayableYn() {
		return payableYn;
	}
	public void setPayableYn(String payableYn) {
		this.payableYn = payableYn;
	}
	public String getInvoiceFileCreateYn() {
		return invoiceFileCreateYn;
	}
	public void setInvoiceFileCreateYn(String invoiceFileCreateYn) {
		this.invoiceFileCreateYn = invoiceFileCreateYn;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAuditId() {
		return auditId;
	}
	public void setAuditId(String auditId) {
		this.auditId = auditId;
	}
	public String getAuditDateTime() {
		return auditDateTime;
	}
	public void setAuditDateTime(String auditDateTime) {
		this.auditDateTime = auditDateTime;
	}
	public String getEditId() {
		return editId;
	}
	public void setEditId(String editId) {
		this.editId = editId;
	}
	public String getEditDateTime() {
		return editDateTime;
	}
	public void setEditDateTime(String editDateTime) {
		this.editDateTime = editDateTime;
	}
	public String getUseYn() {
		return useYn;
	}
	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}
	public String getDeleteDate() {
		return deleteDate;
	}
	public void setDeleteDate(String deleteDate) {
		this.deleteDate = deleteDate;
	}
	
	
}
